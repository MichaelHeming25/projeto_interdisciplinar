$("#financeiro").mouseenter(function () {
    $("#financeiro-more").addClass("more");
    $(".bloco-financeiro").css("border", "1px solid #dfdfdf");
    $(".bloco-financeiro").css("border-radius", "0.3rem");
    $(this).children(".card-info button").css("color", "#317d96");
    $(this)
        .children(".card-info button")
        .children(".card-info button span")
        .css("color", "#317d96");
    $(this)
        .children(".card-info button")
        .children(".card-info button i")
        .css("border-color", "#317d96");
});
$("#financeiro").mouseleave(function () {
    $("#financeiro-more").removeClass("more");
    $(".bloco-financeiro").css("border", "none");
    $(this).children(".card-info button").css("color", "#b7b7b7");
    $(this)
        .children(".card-info button")
        .children(".card-info button span")
        .css("color", "#b7b7b7");
    $(this)
        .children(".card-info button")
        .children(".card-info button i")
        .css("border-color", "#b7b7b7");
});

